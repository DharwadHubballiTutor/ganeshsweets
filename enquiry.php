        var fetchsubcaturl = window.location.origin + "/Admin/Controller/enqcategoryController.php";
        $.getJSON(fetchsubcaturl, function(data) {

            $.each(data, function(index, value) {
                $('#checkboxes').append(
                    $(document.createElement('div')).prop({
                        class: 'form-check'
                    }).append(
                        $(document.createElement('label')).prop({
                            for: 'myCheckBox'
                        }).html(value.catname)
                    ).append(
                        $(document.createElement('input')).prop({
                            class: 'form-check-input',
                            id: 'myCheckBox',
                            name: 'interest_list[]',
                            value: value.CatId,
                            type: 'checkbox'
                        })
                    ).append(document.createElement('br')));
            });
        });
        $('#submit_button').click(function() {
            var validator = $("#enquiry_form").validate();
            if(validator.form()){
            $('#Greetings').modal('show');
            }
        })
        $('#greetingClose').click(function() {
            $('#enquiry_form').submit();
        })
        
        $('#enquiry_form').submit(function(event) {
            var formData = new FormData(this);
            $.ajax({
                type: "POST",
                url: window.location.origin + "/Admin/Controller/newenquiry.php",
                data: formData,
                processData: false,
                contentType: false
            }).done(function(data) {

            });
            location.reload();
        });
    });
